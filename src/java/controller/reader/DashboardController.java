package controller.reader;

import jakarta.servlet.ServletException;
import jakarta.servlet.annotation.WebServlet;
import jakarta.servlet.http.HttpServlet;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import java.io.IOException;

@WebServlet(urlPatterns = "/reader/dashboard")
public class DashboardController extends HttpServlet {

    @Override
    protected void doPost(HttpServletRequest req, HttpServletResponse resp) throws ServletException, IOException {

    }

    @Override
    protected void doGet(HttpServletRequest req, HttpServletResponse resp) throws ServletException, IOException {
        req.setAttribute("pageTitle", "Reader Dashboard");
        req.setAttribute("activeMenu", "dashboard");
        req.setAttribute("contentPage", "/view/reader/dashboard.jsp");
        req.getRequestDispatcher("/include/reader/layout.jsp")
                .forward(req, resp);
    }
}
